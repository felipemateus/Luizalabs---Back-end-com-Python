'''
Boas práticas para nomear variáveis e constantes em Python:

1. Use nomes descritivos: Escolha nomes que descrevam claramente o propósito da variável ou constante.
2. Utilize letras minúsculas e underscores: Para variáveis, use letras minúsculas e separe palavras com underscores (snake_case). Exemplo: minha_variavel.
3. Constantes em maiúsculas: Para constantes, use letras maiúsculas e underscores para separar palavras (UPPER_SNAKE_CASE). Exemplo: MINHA_CONSTANTE.
4. Evite abreviações: Abreviações podem tornar o código menos legível. Prefira nomes completos.
5. Seja consistente: Mantenha um padrão consistente ao longo do código para facilitar a leitura e manutenção.  
6. Evite nomes reservados: Não use palavras reservadas do Python como nomes de variáveis ou constantes.

'''
